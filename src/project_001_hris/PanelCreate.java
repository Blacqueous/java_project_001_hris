    public void comboBoxLoad(JComboBox comboBox, String tablename) {

        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        dcbm.addElement("");

        try {
            //Database connection
            ClassMyDBConnectionSQL.init();
            try (Connection c = ClassMyDBConnectionSQL.getMyConnection()) {
                PreparedStatement ps;
                ResultSet rs;
                
                ps = c.prepareStatement("SELECT name FROM " + tablename);
                
                rs = ps.executeQuery();
                while (rs.next()) {
                    String a = rs.getString(1);
                    dcbm.addElement(a);
                }
                comboBox.setModel(dcbm);
                comboBox.setPrototypeDisplayValue("X");
                
                // Close excess connection
            }

        } catch (SQLException ex) {
            Logger.getLogger(FrameMain.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

