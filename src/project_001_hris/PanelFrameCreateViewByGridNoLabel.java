    public void comboBoxLoadFromDB(String table, JComboBox cmbbox, String title, ArrayList listId) {

        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        dcbm.addElement("");
        
        try {
            
            //Database connection
            ClassMyDBConnectionEmbeddedDerby.initDB();
            
            try (Connection c = ClassMyDBConnectionEmbeddedDerby.getMyConnection()) {
                
                PreparedStatement ps;
                ResultSet rs;
                int ct;
                
                ps = c.prepareStatement("SELECT id, name FROM " + table + " ORDER BY name ASC");
                rs = ps.executeQuery();
                ct = 0;
                
                while (rs.next()) {
                    listId.add(ct, Integer.parseInt(rs.getString(1)));
                    dcbm.addElement(rs.getString(2).trim());
                    ct++;
                }
                
                cmbbox.setModel(dcbm);
                ClassComplexCellRenderer renderer = new ClassComplexCellRenderer();
                renderer.title = title;
                cmbbox.setRenderer(renderer);

                // Close excess connection
                c.close();

            }
            
        } catch (SQLException ex) {
            Logger.getLogger(FrameMain.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void comboBoxLoadFromConstants(ArrayList array, JComboBox cmbbox, String title) {

        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        dcbm.addElement("");
        
        for (int i = 0; i < array.size(); i++) {
            dcbm.addElement(array.get(i));
        }
        
        cmbbox.setModel(dcbm);
        ClassComplexCellRenderer renderer = new ClassComplexCellRenderer();
        renderer.title = title;
        cmbbox.setRenderer(renderer);

    }

