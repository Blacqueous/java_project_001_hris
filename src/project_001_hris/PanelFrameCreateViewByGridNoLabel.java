    public String formatDate(JDateChooser datechooser) {

        if (datechooser.getDate() == null) {
            return "";
        }
        
        java.text.SimpleDateFormat fmt = new java.text.SimpleDateFormat("yyyy-MM-dd 00:00:00");
        String formattedDate = fmt.format(datechooser.getDate());
        
        if (!formattedDate.isEmpty()) {
            return formattedDate;
        } else {
            return "";
        }

    }

