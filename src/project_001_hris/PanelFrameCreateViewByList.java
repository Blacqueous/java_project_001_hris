    public void setFieldsForSwitch(String[] field) {
        
        if(field != null) {
            // Basic Information
            txtfldNameFirst.setText(field[0]);
            txtfldNameMiddle.setText(field[1]);
            txtfldNameLast.setText(field[2]);
            txtfldPlantillaNum.setText(field[3]);
            cmbboxPosition.setSelectedItem(field[4]);
            cmbboxDepartment.setSelectedItem(field[5]);
            cmbboxCategory.setSelectedItem(field[6]);
            dateHired.setDate(fmtDateVal.formatStringToDate(field[7]));
            if(field[8].equals("")) {
                txtfldSalary.setText("");
            } else {
                txtfldSalary.setValue(Integer.parseInt(field[8]));
            }
            
            // Personal Information
            dateBirth.setDate(fmtDateVal.formatStringToDate(field[9]));
            txtfldAddress1.setText(field[10]);
            txtfldAddress2.setText(field[11]);
            cmbboxGender.setSelectedItem(field[12]);
            cmbboxCivilStatus.setSelectedItem(field[13]);
            txtfldNationality.setText(field[14]);
            txtfldReligion.setText(field[15]);
            txtfldEmail.setText(field[16]);
            txtfldContactTele.setText(field[17]);
            txtfldContactCell.setText(field[18]);
            txtfldContactOthers.setText(field[19]);

            // Additional Information
            if(field[20].equals("")) {
                txtfldCodeSSS.setText("");
            } else {
                txtfldCodeSSS.setValue(field[20]);
            }
            if(field[21].equals("")) {
                txtfldCodeTIN.setText("");
            } else {
                txtfldCodeTIN.setValue(field[21]);
            }
            txtfldCodeGSIS.setText(field[22]);
            if(field[23].equals("")) {
                txtfldCodePHIC.setText("");
            } else {
                txtfldCodePHIC.setValue(field[23]);
            }
            if(field[24].equals("")) {
                txtfldCodePAGIBIG.setText("");
            } else {
                txtfldCodePAGIBIG.setValue(field[24]);
            }
        }
        
    }

